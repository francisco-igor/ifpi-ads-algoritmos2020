# Leia 3 números, calcule e escreva a média dos números.

# ENTRADA
num1 = int(input('Leia um número: '))
num2 = int(input('Leia outro número: '))
num3 = int(input('Leia mais um número: '))

# PROCESSAMENTO
media = (num1 + num2 + num3) / 3

# SAÍDA
print(f'A média dos números é {media}')
